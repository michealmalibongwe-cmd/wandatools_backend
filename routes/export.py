def _safe_name(text: str) -> str:
    return re.sub(r"[^A-Za-z0-9]", "_", (text or "WandaTools").strip())[:28]


def _fmt(amount: float, currency: str) -> str:
    return f"{currency} {amount:,.2f}"


def _pct(num: float, denom: float) -> str:
    return f"{num / denom * 100:.1f}%" if denom else "—"


def _get_period(
    year: int,
    start_date: Optional[str],
    end_date: Optional[str],
) -> tuple:
    if start_date and end_date:
        try:
            sd = datetime.fromisoformat(start_date.replace("Z", "+00:00")).replace(tzinfo=None)
            ed = datetime.fromisoformat(end_date.replace("Z", "+00:00")).replace(tzinfo=None)
            return sd, ed
        except ValueError:
            raise HTTPException(
                status_code=status.HTTP_400_BAD_REQUEST,
                detail="Invalid start_date or end_date — use YYYY-MM-DD",
            )
    return datetime(year, 1, 1), datetime(year, 12, 31, 23, 59, 59)


def _fetch_txns(db: Session, user_id: int, start_dt: datetime, end_dt: datetime) -> list:
    return (
        db.query(Transaction)
        .filter(
            Transaction.user_id          == user_id,
            Transaction.transaction_date >= start_dt,
            Transaction.transaction_date <= end_dt,
            Transaction.is_deleted       == False,   # noqa: E712
        )
        .order_by(Transaction.transaction_date)
        .all()
    )


def _by_cat(txns: list, txn_type: str) -> dict:
    result: dict = {}
    for t in txns:
        tv = t.type.value if hasattr(t.type, "value") else str(t.type)
        if tv == txn_type:
            result[t.category] = result.get(t.category, 0.0) + t.amount
    return dict(sorted(result.items(), key=lambda x: x[1], reverse=True))


def _monthly_flows(txns: list, start_dt: datetime, end_dt: datetime) -> dict:
    raw: dict = defaultdict(lambda: {"income": 0.0, "expense": 0.0})
    for t in txns:
        k  = t.transaction_date.strftime("%Y-%m")
        tv = t.type.value if hasattr(t.type, "value") else str(t.type)
        raw[k]["income" if tv == "income" else "expense"] += t.amount

    result = {}
    cur = datetime(start_dt.year, start_dt.month, 1)
    end = datetime(end_dt.year,   end_dt.month,   1)
    while cur <= end:
        k = cur.strftime("%Y-%m")
        d = raw.get(k, {"income": 0.0, "expense": 0.0})
        result[k] = {"income": d["income"], "expense": d["expense"],
                     "net": d["income"] - d["expense"]}
        cur = datetime(cur.year + 1, 1, 1) if cur.month == 12 else \
              datetime(cur.year, cur.month + 1, 1)
    return result


def _expense_tax_breakdown(txns: list) -> dict:
    result: dict = {}
    for t in txns:
        tv = t.type.value if hasattr(t.type, "value") else str(t.type)
        if tv == "expense":
            if t.category not in result:
                result[t.category] = [0.0, 0.0]
            result[t.category][0] += t.amount
            if t.tax_deductible:
                result[t.category][1] += t.amount
    return dict(sorted(result.items(), key=lambda x: x[1][0], reverse=True))


