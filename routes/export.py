class _FooterCanvas(rl_canvas.Canvas):
    def __init__(self, *args, generated_at: str = "", **kwargs):
        rl_canvas.Canvas.__init__(self, *args, **kwargs)
        self._saved_page_states: list = []
        self._generated_at = generated_at

    def showPage(self):
        self._saved_page_states.append(dict(self.__dict__))
        self._startPage()

    def save(self):
        total = len(self._saved_page_states)
        for state in self._saved_page_states:
            self.__dict__.update(state)
            self._draw_footer(total)
            rl_canvas.Canvas.showPage(self)
        rl_canvas.Canvas.save(self)

    def _draw_footer(self, total: int) -> None:
        pg = self._pageNumber
        self.saveState()
        # Light strip
        self.setFillColor(C_LIGHT)
        self.rect(0, 0, PAGE_W, 1.5 * cm, fill=1, stroke=0)
        # Blue rule above strip
        self.setStrokeColor(C_BLUE)
        self.setLineWidth(0.75)
        self.line(MARGIN, 1.6 * cm, PAGE_W - MARGIN, 1.6 * cm)
        # Confidentiality notice
        self.setFont("Helvetica-Oblique", 6.5)
        self.setFillColor(C_MID)
        self.drawString(
            MARGIN, 0.65 * cm,
            f"Generated by WandaTools – Confidential  |  "
            f"Auto-generated from WandaTools data as of {self._generated_at}",
        )
        # Page number
        self.setFont("Helvetica-Bold", 7)
        self.setFillColor(C_DARK)
        self.drawRightString(PAGE_W - MARGIN, 0.65 * cm, f"Page {pg} of {total}")
        self.restoreState()


def _make_canvas_cls(generated_at: str):
    class _C(_FooterCanvas):
        def __init__(self, *args, **kwargs):
            super().__init__(*args, generated_at=generated_at, **kwargs)
    return _C


