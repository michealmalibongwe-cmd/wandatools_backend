def _loan_score(total_income: float, total_expenses: float, flows: dict) -> tuple:
    score = 0
    bd: dict = {}

    # Coverage ratio (30 pts)
    ratio = (total_income / total_expenses) if total_expenses else (2.0 if total_income else 0)
    pts   = 30 if ratio >= 2.0 else 22 if ratio >= 1.5 else 15 if ratio >= 1.2 else 8 if ratio >= 1.0 else 0
    score += pts;  bd["Income Coverage Ratio"] = (pts, 30)

    # Income stability — coeff. of variation (30 pts)
    incomes = [v["income"] for v in flows.values()]
    if len(incomes) >= 2 and sum(incomes) > 0:
        m  = sum(incomes) / len(incomes)
        cv = math.sqrt(sum((x - m) ** 2 for x in incomes) / len(incomes)) / m if m else 1
        pts = 30 if cv <= 0.10 else 24 if cv <= 0.20 else 16 if cv <= 0.35 else 8 if cv <= 0.50 else 3
    else:
        pts = 10
    score += pts;  bd["Monthly Income Stability"] = (pts, 30)

    # History depth (20 pts)
    months_active = sum(1 for v in flows.values() if v["income"] > 0)
    pts = 20 if months_active >= 12 else 14 if months_active >= 6 else 8 if months_active >= 3 else 3
    score += pts;  bd["Financial History Depth"] = (pts, 20)

    # Profitability consistency (20 pts)
    n   = max(len(flows), 1)
    pct = sum(1 for v in flows.values() if v["net"] >= 0) / n
    pts = 20 if pct >= 0.90 else 15 if pct >= 0.75 else 9 if pct >= 0.50 else 3
    score += pts;  bd["Profitability Consistency"] = (pts, 20)

    score = min(score, 100)
    rating = "Excellent" if score >= 80 else "Good" if score >= 65 else "Fair" if score >= 50 else "Needs Improvement"
    return score, bd, rating


def _invest_score(total_income: float, total_expenses: float,
                  income_by_cat: dict, flows: dict) -> tuple:
    score = 0
    bd: dict = {}

    # Revenue scale (25 pts)
    pts = (25 if total_income >= 500_000 else 20 if total_income >= 100_000
           else 14 if total_income >= 50_000 else 8 if total_income >= 10_000 else 3)
    score += pts;  bd["Revenue Scale"] = (pts, 25)

    # Profit margin (25 pts)
    margin = (total_income - total_expenses) / total_income if total_income else 0
    pts    = (25 if margin >= 0.40 else 20 if margin >= 0.25 else 14 if margin >= 0.15
              else 8 if margin >= 0.05 else 3 if margin >= 0 else 0)
    score += pts;  bd["Profit Margin"] = (pts, 25)

    # Revenue diversification — inverse HHI (20 pts)
    if income_by_cat and total_income:
        shares = [v / total_income for v in income_by_cat.values()]
        pts    = min(round((1 - sum(s ** 2 for s in shares)) * 20), 20)
    else:
        pts = 0
    score += pts;  bd["Revenue Diversification"] = (pts, 20)

    # Growth trend (30 pts)
    fl = list(flows.values())
    if len(fl) >= 4:
        half = len(fl) // 2
        avg1 = sum(v["income"] for v in fl[:half])  / half
        avg2 = sum(v["income"] for v in fl[half:])  / (len(fl) - half)
        g    = (avg2 - avg1) / avg1 if avg1 else 0
        pts  = 30 if g >= 0.30 else 23 if g >= 0.15 else 16 if g >= 0.05 else 10 if g >= 0 else 4
    else:
        pts = 10
    score += pts;  bd["Revenue Growth Trend"] = (pts, 30)

    score  = min(score, 100)
    rating = "Excellent" if score >= 80 else "Good" if score >= 65 else "Fair" if score >= 50 else "Needs Improvement"
    return score, bd, rating


